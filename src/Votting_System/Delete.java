    public void equal()
    {
        try
        {
            String sql="Select Username,Password From Regt where Username=? and Password=?";
            ps=conn.prepareStatement(sql);
            ps.setString(1, jTextField1.getText());
            ps.setString(2, String.valueOf(jPasswordField1.getPassword()));
            rs=ps.executeQuery();
            if(rs.next())
            {
                user=rs.getString(1);
                pass=rs.getString(2);
            }
            rs.close();
            ps.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
