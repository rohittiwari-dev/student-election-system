    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        equal();    
        if("".equals(jTextField1.getText())||"".equals(String.valueOf(jPasswordField1.getPassword())))
        {
            JOptionPane.showMessageDialog(null, "Compelete Your Detail");
        }
        else
        {
            if(jTextField1.getText().equals(user) && String.valueOf(jPasswordField1.getPassword()).equals(pass))
            {
                if(JOptionPane.YES_OPTION==JOptionPane.showConfirmDialog(null, "Please Confirm","Confimation",JOptionPane.YES_NO_OPTION))
                {   
                    try
                    {
                        String asql ="DELETE FROM Regt  WHERE Username=?";
                        ps=conn.prepareStatement(asql);
                        ps.setString(1, jTextField1.getText());
                        ps.execute();
                        JOptionPane.showMessageDialog(null, "Account Deleted Succesfuly");
                        Login t= new Login ();
                        t.setVisible(true);
                        this.dispose();
                        ps.close();
                    }catch(Exception e)
                    {
                       JOptionPane.showMessageDialog(null, e);
                    }
                 }
                else
                    JOptionPane.showMessageDialog(null, "Account Deletion was Canceled");
             }
            else 
                JOptionPane.showMessageDialog(null, "Incorrect Username or Password");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

